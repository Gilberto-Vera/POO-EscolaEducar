    private void carregarLista(){
        TableModel modeloDadosAluno = new AbstractTableModel() {
            private List<Aluno> alunos = Banco.getAlunos();
            private String[] colunas = new String[]{"Matr√≠cula", "Nome", "Curso"};
            @Override
            public int getRowCount() {
                return alunos.size();
            }

            @Override
            public int getColumnCount() {
                return 3;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                Object valorCelula = null;
                switch(columnIndex) {
                    case 0:
                        valorCelula = alunos.get(rowIndex).getMatricula();
                        break;
                    case 1:
                        valorCelula = alunos.get(rowIndex).getNome();
                        break;
                    case 2:
                        valorCelula = alunos.get(rowIndex).getCurso().getNome();
                        break;
                }
                return valorCelula;
            }

            @Override
            public String getColumnName(int column) {
                return colunas[column];
            }
            
        };
        tblAluno.setModel(modeloDadosAluno);
        
    }
    
